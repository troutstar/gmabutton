void draw_char(uint16_t *fb, int x, int y, char c, uint16_t color, int scale)
{
    if (c < 0x20 || c > 0x7E) c = '?';
    const uint8_t *glyph = s_font5x7[c - 0x20];
    for (int col = 0; col < 5; col++) {
        uint8_t line = glyph[col];
        for (int row = 0; row < 7; row++) {
            if (line & (1 << row)) {
                draw_fill_rect(fb, x + col * scale, y + row * scale,
                               scale, scale, color);
            }
        }
    }
}

void draw_str(uint16_t *fb, int x, int y, const char *s, uint16_t color, int scale)
{
    while (*s) {
        draw_char(fb, x, y, *s++, color, scale);
        x += 6 * scale;
    }
}

void draw_str_centered(uint16_t *fb, int y, const char *s, uint16_t color, int scale)
{
    int len = 0; for (const char *p = s; *p; ++p) len++;
    int w   = len * 6 * scale;
    int x   = (SCREEN_W - w) / 2;
    if (x < 0) x = 0;
    draw_str(fb, x, y, s, color, scale);
}

void draw_hbar(uint16_t *fb, int x, int y, int w, int h, float fill, uint16_t color)
{
    draw_rect(fb, x, y, w, h, COL_GRAY);
    int filled = (int)(fill * (w - 2));
    if (filled > 0)
        draw_fill_rect(fb, x + 1, y + 1, filled, h - 2, color);
}

void draw_filled_circle(uint16_t *fb, int cx, int cy, int r, uint16_t color)
{
    if (r <= 0) return;
    int y_lo = g_fb_y_offset;
    int y_hi = g_fb_y_offset + STRIP_H;
    for (int gy = y_lo; gy < y_hi; ++gy) {
        int dy = gy - cy;
        if (dy > r || dy < -r) continue;
        int dx = (int)sqrtf((float)(r * r - dy * dy));
        int x0 = cx - dx; if (x0 < 0) x0 = 0;
        int x1 = cx + dx; if (x1 >= SCREEN_W) x1 = SCREEN_W - 1;
        int fy = gy - g_fb_y_offset;
        uint16_t *row = fb + fy * SCREEN_W;
        for (int x = x0; x <= x1; ++x) row[x] = color;
    }
}
