#include "draw.h"
#include <string.h>
#include <stdlib.h>
#include <math.h>

int g_fb_y_offset = 0;

static inline void put_pixel(uint16_t *fb, int x, int y, uint16_t color)
{
    int fy = y - g_fb_y_offset;
    if ((unsigned)x < SCREEN_W && (unsigned)fy < STRIP_H)
        fb[fy * SCREEN_W + x] = color;
}

void draw_pixel(uint16_t *fb, int x, int y, uint16_t color)
{
    put_pixel(fb, x, y, color);
}

void draw_fill(uint16_t *fb, uint16_t color)
{
    if (color == 0) {
        memset(fb, 0, SCREEN_W * STRIP_H * sizeof(uint16_t));
    } else {
        for (int i = 0; i < SCREEN_W * STRIP_H; i++) fb[i] = color;
    }
}

/* Multiply each channel by ~15/16 toward black. Operates on the current strip
   only (STRIP_H rows, not SCREEN_H — fixed from the klipperbridge version). */
void draw_fade(uint16_t *fb)
{
    for (int i = 0; i < SCREEN_W * STRIP_H; i++) {
        uint16_t be  = fb[i];
        uint16_t px  = (be >> 8) | (be << 8);
        uint16_t r   = (px >> 11) & 0x1F;
        uint16_t g   = (px >>  5) & 0x3F;
        uint16_t b   =  px        & 0x1F;
        r = (r * 15) >> 4;
        g = (g * 15) >> 4;
        b = (b * 15) >> 4;
        uint16_t out = (r << 11) | (g << 5) | b;
        fb[i] = (out >> 8) | (out << 8);
    }
}

void draw_line(uint16_t *fb, int x0, int y0, int x1, int y1, uint16_t color)
{
    int dx = abs(x1 - x0), sx = x0 < x1 ? 1 : -1;
    int dy = abs(y1 - y0), sy = y0 < y1 ? 1 : -1;
    int err = (dx > dy ? dx : -dy) / 2, e2;
    while (1) {
        put_pixel(fb, x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        e2 = err;
        if (e2 > -dx) { err -= dy; x0 += sx; }
        if (e2 <  dy) { err += dx; y0 += sy; }
    }
}

void draw_rect(uint16_t *fb, int x, int y, int w, int h, uint16_t color)
{
    draw_line(fb, x,     y,     x+w-1, y,     color);
    draw_line(fb, x,     y+h-1, x+w-1, y+h-1, color);
    draw_line(fb, x,     y,     x,     y+h-1, color);
    draw_line(fb, x+w-1, y,     x+w-1, y+h-1, color);
}

void draw_fill_rect(uint16_t *fb, int x, int y, int w, int h, uint16_t color)
{
    for (int row = y; row < y + h; row++)
        for (int col = x; col < x + w; col++)
            put_pixel(fb, col, row, color);
}

